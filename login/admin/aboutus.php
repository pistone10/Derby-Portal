<br>
The PHP Login Script was originally the brainchild of a PHP coder named Jpmaster77 a.k.a. The Grandmaster of C++ (GMC). 
It quickly became an instant hit amongst web designers when the code was put up for download on the Evolt website as early as 2004. 
In fact the original link is still going strong <a href="http://www.evolt.org/node/60384">here</a> with nearly 2700 comments and probably many many more downloads.
<br><br>
The original source code was only intended as a description of the implementation of an advanced Login System with PHP and MySQL but JPMaster77 continued to support and
advise on ways to implement the script but has not logged on to the Evolt website since 2008. 
<br><br>
The original script is now over 7 years old and PHP, databases, security implementations and more have come
on a long way in that time so it has been the intention of Angry Frog to both update and improve the Login script so it is more secure, has more features, 
and is both more user friendly to the registering user and the administrator alike with easy to use GUIs on the new back-end administration panel. 

